def seed_prompts(force_upgrade=False):
    """
    Загрузить промты в БД.
    Если таблица пуста — полный сид.
    Если старые промты без system_prompt — пересоздать каталог (force через main).
    """
    from database import (
        count_prompts, add_prompt, get_db_connection, reseed_prompts_if_outdated,
    )

    total = count_prompts()
    _, with_system = reseed_prompts_if_outdated()

    # Пустая БД
    if total == 0:
        for p in PROMPTS:
            add_prompt(
                p['category'], p['title'], p['prompt_text'],
                sort_order=p.get('sort_order', 0),
                description=p.get('description'),
                system_prompt=p.get('system_prompt'),
                variables=p.get('variables'),
                icon=p.get('icon', '📌'),
            )
        return len(PROMPTS)

    # Старый каталог (все без system_prompt) — заменить на новый магазин
    if force_upgrade or (total > 0 and with_system == 0):
        conn = get_db_connection()
        conn.execute('DELETE FROM prompts')
        conn.commit()
        conn.close()
        for p in PROMPTS:
            add_prompt(
                p['category'], p['title'], p['prompt_text'],
                sort_order=p.get('sort_order', 0),
                description=p.get('description'),
                system_prompt=p.get('system_prompt'),
                variables=p.get('variables'),
                icon=p.get('icon', '📌'),
            )
        return len(PROMPTS)

    return 0
