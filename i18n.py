def category_name(category, language):
    language = normalize_language(language)
    return CATEGORY_TRANSLATIONS.get(language, CATEGORY_TRANSLATIONS['en']).get(
        category, category.title()
    )


def prompt_title(title, language):
    language = normalize_language(language)
    if language == 'ru':
        return title
    return PROMPT_TITLE_TRANSLATIONS.get(title, {}).get(language, title)


def variable_hint(variable, language):
    key = f'var_{variable}'
    translated = t(language, key)
    return translated if translated != key else variable


def response_language_instruction(language):
    names = {
        'ru': 'русском',
        'en': 'English',
        'uk': 'українській',
        'de': 'Deutsch',
        'es': 'español',
    }
    language = normalize_language(language)
    if language == 'en':
        return 'Always answer in English unless the user explicitly asks for another language.'
    return f'Всегда отвечай на {names[language]} языке, если пользователь явно не попросил другой.'
